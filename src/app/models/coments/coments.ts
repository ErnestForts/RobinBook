export class Coments {
    public Nombre: string;
    public Apellido: string;
    public Foto: string;
    public Coment: string;

    constructor(Nombre: string, Apellido: string, Foto: string, Coment: string){ 
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.Foto = Foto;
        this.Coment = Coment;
    }
}